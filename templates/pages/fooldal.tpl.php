<h2>Móra Ferenc Gimnázium</h2>
<p>

    A Móra Ferenc Gimnázium Kiskunfélegyháza legrégibb és legismertebb oktatási intézménye. A reformkorban az anyanyelv és a matematika oktatását részesítették előnyben. Igazi fejlődésnek Holló László (Holló László festőművész édesapja) igazgatósága alatt indult, aki a gimnázium főgimnáziummá fejlesztését tűzte ki célul. A város lakosságának növekedése, a polgárosodás, műveltségi igények miatt e címet 1891-től viselhette minisztériumi engedély alapján Városi Katolikus Főgimnázium névvel.
    Innentől kezdve a régi iskolaépület szűknek bizonyult.
</p>
<p class="idezet">
„Sic itur ad astra. (Vergilius)”
</p>

<p>A jelenlegi épület 1896-ban épült egy katonai istálló helyén, annak tégláinak felhasználásával Alpár Ignác tervei alapján eklektikus stílusban. Az avatóünnepséget felvették az országos millenniumi ünnepségek listájára. Az avatóra az akkor még fiatal gimnazista Móra Ferenc írt verset. A román megszállás alatt az épület megrongálódott, csak 1921/22-es tanévben vált ismét rendszeressé az oktatás. Ekkor felvette Szent László nevét. 1944-ben katonai kórház működött itt, a legkorszerűbb pedagógiai módszerekkel történő tanítást immár a másik épületben tartották.

    1978-ban haladta meg először az érettségizők száma a százat. A 80-as évek végén új épületrész került átadásra, ahol biológiai labor létesült. 1990-től fokozatosan emelkedett a tanulók létszáma. 1994-ben indult a hatosztályos képzés, az iskola egyik pillére. 2000-ben Millenniumi Parkot adtak át fekete obeliszkkel és dísznövényekkel. 2004-ben ötosztályos speciális nyelvi képzés indult a már meglévő emelt óraszámú nyelvi tagozatok mellett.

    Az intézmény első emeletén egykori diákjainak, köztük Szántó Piroskának és Holló Lászlónak a festményei láthatóak.

    Az intézmény 2009-ben ünnepelte fennállásának kétszáz éves évfordulóját, a 2009-2010-es tanévvel. Az évforduló alkalmából szobrot lepleztek le az egykori igazgató, Holló László tiszteletére és antológia jelent meg a gimnáziumba járó diákok verseiből. 2010 tavaszán Kétszáz év művészete címmel kiállítást rendeznek a gimnázium volt diákjainak munkáiból.
</p>

<p>
